    /**
     * Import (upsert) notes data into IndexedDB.
     * Uses put() per item matched by uuid — NEVER clears the store.
     */
    async function importNotesData(data) {
        if (!data || !data.folders || !data.items) return;
        const db = await AppDB.initDB();

        // ── Upsert folders ────────────────────────────────────────────────────
        await new Promise((resolve, reject) => {
            const tx = db.transaction([AppDB.FOLDERS_STORE], 'readwrite');
            const store = tx.objectStore(AppDB.FOLDERS_STORE);

            let pending = data.folders.length;
            if (pending === 0) { tx.oncomplete = resolve; return; }

            for (const folder of data.folders) {
                const { id: _dropped, ...rest } = folder;

                if (rest.uuid) {
                    const req = store.index('uuid').get(rest.uuid);
                    req.onsuccess = () => {
                        if (req.result) {
                            store.put({ ...req.result, ...rest });
                        } else {
                            store.add(rest);
                        }
                        if (--pending === 0) { /* wait for oncomplete */ }
                    };
                } else {
                    store.put({ ...rest, id: _dropped });
                }
            }

            tx.oncomplete = resolve;
            tx.onerror = reject;
        });

        // ── Upsert items ──────────────────────────────────────────────────────
        await new Promise((resolve, reject) => {
            const tx = db.transaction([AppDB.ITEMS_STORE], 'readwrite');
            const store = tx.objectStore(AppDB.ITEMS_STORE);

            let pending = data.items.length;
            if (pending === 0) { tx.oncomplete = resolve; return; }

            for (const item of data.items) {
                const copy = { ...item };

                // Legacy support for base64
                if (copy._hasBlobData && typeof copy.fileBlob === 'string') {
                    delete copy.fileBlob;
                    delete copy._hasBlobData;
                }

                const { id: _dropped, ...rest } = copy;

                if (rest.uuid) {
                    const req = store.index('uuid').get(rest.uuid);
                    req.onsuccess = () => {
                        if (req.result) {
                            store.put({ ...req.result, ...rest });
                        } else {
                            store.add(rest);
                        }
                        if (--pending === 0) { /* wait for oncomplete */ }
                    };
                } else {
                    store.put({ ...rest, id: _dropped });
                }
            }

            tx.oncomplete = resolve;
            tx.onerror = reject;
        });
    }

