    /**
     * Merge local and cloud notes data.
     * Deduplicates by `uuid`. Higher `version` wins. Timestamp tiebreaker.
     * Tombstoned items are excluded.
     */
    function _mergeNotesData(localData, cloudData, tombstones) {
        let localWon = false;

        // Map of uuid -> max tombstone version
        const tombstoneMap = new Map();
        if (tombstones && tombstones.items) {
            for (const t of tombstones.items) {
                const currentMax = tombstoneMap.get(t.uuid) || 0;
                const tVer = t.version || 999999;
                if (tVer > currentMax) tombstoneMap.set(t.uuid, tVer);
            }
        }

        function isTombstoned(itemOrFolder) {
            const key = itemOrFolder.uuid || String(itemOrFolder.id);
            const tombstoneVersion = tombstoneMap.get(key);
            if (tombstoneVersion === undefined) return false;
            const itemVer = itemOrFolder.version || 1;
            return tombstoneVersion > itemVer;
        }

        // ── FOLDERS ───────────────────────────────────────────────────────────
        const folderMap = new Map();

        if (cloudData && cloudData.folders) {
            for (const f of cloudData.folders) {
                const key = f.uuid || String(f.id);
                if (!isTombstoned(f)) folderMap.set(key, f);
            }
        }
        if (localData && localData.folders) {
            for (const f of localData.folders) {
                const key = f.uuid || String(f.id);
                if (isTombstoned(f)) continue;

                if (!folderMap.has(key)) {
                    folderMap.set(key, f);
                    localWon = true;
                } else {
                    const existing = folderMap.get(key);
                    const localVer = f.version || 1;
                    const cloudVer = existing.version || 1;

                    if (localVer > cloudVer) {
                        folderMap.set(key, f);
                        localWon = true;
                    } else if (localVer === cloudVer) {
                        const existingTime = new Date(existing.updatedAt || existing.updated_at || existing.createdAt || existing.created_at || 0).getTime();
                        const localTime = new Date(f.updatedAt || f.createdAt || 0).getTime();
                        if (localTime > existingTime) {
                            folderMap.set(key, f);
                            localWon = true;
                        }
                    }
                }
            }
        }

        // ── ITEMS ─────────────────────────────────────────────────────────────
        const itemMap = new Map();

        if (cloudData && cloudData.items) {
            for (const item of cloudData.items) {
                const key = item.uuid || String(item.id);
                if (!isTombstoned(item)) itemMap.set(key, item);
            }
        }
        if (localData && localData.items) {
            for (const item of localData.items) {
                const key = item.uuid || String(item.id);
                if (isTombstoned(item)) continue;

                if (!itemMap.has(key)) {
                    itemMap.set(key, item);
                    localWon = true;
                } else {
                    const existing = itemMap.get(key);
                    const localVer = item.version || 1;
                    const cloudVer = existing.version || 1;

                    if (localVer > cloudVer) {
                        itemMap.set(key, item);
                        localWon = true;
                    } else if (localVer === cloudVer) {
                        const existingTime = new Date(existing.updatedAt || existing.updated_at || existing.createdAt || existing.created_at || 0).getTime();
                        const localTime = new Date(item.updatedAt || item.createdAt || 0).getTime();
                        if (localTime > existingTime) {
                            itemMap.set(key, item);
                            localWon = true;
                        }
                    }
                }
            }
        }

        return {
            data: {
                folders: Array.from(folderMap.values()),
                items: Array.from(itemMap.values())
            },
            localWon
        };
    }

    /**
     * Physically delete items from local IndexedDB that match valid tombstones.
     */
    async function _applyTombstonesLocally(tombstones, localData) {
        if (!tombstones || !tombstones.items || tombstones.items.length === 0) return;
        if (!localData) return;

        const tombstoneMap = new Map();
        for (const t of tombstones.items) {
            const currentMax = tombstoneMap.get(t.uuid) || 0;
            const tVer = t.version || 999999;
            if (tVer > currentMax) tombstoneMap.set(t.uuid, tVer);
        }

        function isTombstoned(itemOrFolder) {
            const key = itemOrFolder.uuid || String(itemOrFolder.id);
            const tombstoneVersion = tombstoneMap.get(key);
            if (tombstoneVersion === undefined) return false;
            return tombstoneVersion > (itemOrFolder.version || 1);
        }

        const idsToDeleteItems = (localData.items || []).filter(i => isTombstoned(i)).map(i => i.id);
        const idsToDeleteFolders = (localData.folders || []).filter(f => isTombstoned(f)).map(f => f.id);

        if (idsToDeleteItems.length === 0 && idsToDeleteFolders.length === 0) return;

        const db = await AppDB.initDB();

        if (idsToDeleteItems.length > 0) {
            await new Promise(resolve => {
                const tx = db.transaction([AppDB.ITEMS_STORE], 'readwrite');
                const store = tx.objectStore(AppDB.ITEMS_STORE);
                idsToDeleteItems.forEach(id => store.delete(id));
                tx.oncomplete = resolve;
                tx.onerror = resolve;
            });
        }

        if (idsToDeleteFolders.length > 0) {
            await new Promise(resolve => {
                const tx = db.transaction([AppDB.FOLDERS_STORE], 'readwrite');
                const store = tx.objectStore(AppDB.FOLDERS_STORE);
                idsToDeleteFolders.forEach(id => store.delete(id));
                tx.oncomplete = resolve;
                tx.onerror = resolve;
            });
        }
    }

